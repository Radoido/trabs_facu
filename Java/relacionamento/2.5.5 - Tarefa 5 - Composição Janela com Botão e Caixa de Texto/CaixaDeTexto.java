public class CaixaDeTexto{
   
    private String texto;



    public CaixaDeTexto(){
        this.texto = "";

    }

    public CaixaDeTexto(String texto){
        this.texto = texto;
    }

    public String getTexto() {
        return texto;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }



   
}
