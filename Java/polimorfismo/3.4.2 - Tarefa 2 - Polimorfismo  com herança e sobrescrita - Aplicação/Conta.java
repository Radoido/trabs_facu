public class Conta{

    private double saldo;


    public Conta(){
        this.saldo = 0;
    }

    public Conta(double saldo){
        this.saldo = saldo;
    }

    public double getSaldo() {
        return saldo;
    }

    public void setSaldo(double saldo) {
        this.saldo = saldo;
    }

    


}